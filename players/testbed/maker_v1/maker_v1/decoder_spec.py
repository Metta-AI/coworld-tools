def _render_decoder_impl(spec: dict[str, Any]) -> str:
    input_kind = str(spec["input_kind"])
    strategy = str(spec["decoder_strategy"])
    hints = spec["extracted_hints"]
    width, height = _dimension_from_hints(hints)
    expected_bytes = _expected_bytes_from_hints(hints)
    bit_depth = _bit_depth_from_hints(hints)
    can_decode_packed_4bit = (
        input_kind == "raw_visual_observation"
        and width is not None
        and height is not None
        and expected_bytes is not None
        and bit_depth == 4
        and "packed" in hints.get("codec_terms", [])
    )
    spec_literal = pprint.pformat(spec, sort_dicts=True, width=100)
    return f'''from __future__ import annotations

import hashlib
import json
import struct
import zlib
from dataclasses import dataclass
from pathlib import Path
from typing import Any


DECODER_NAME = "generated_{_test_module_prefix(str(spec["game_slug"]))}_decoder"
DECODER_VERSION = "maker.decoder_impl.v1"
DECODER_SPEC: dict[str, Any] = {spec_literal}
INPUT_KIND = {input_kind!r}
DECODER_STRATEGY = {strategy!r}
FRAME_WIDTH = {width!r}
FRAME_HEIGHT = {height!r}
EXPECTED_SOURCE_BYTES = {expected_bytes!r}
BIT_DEPTH = {bit_depth!r}
CAN_DECODE_PACKED_4BIT = {can_decode_packed_4bit!r}


class DecodeError(ValueError):
    pass


@dataclass(frozen=True, slots=True)
class DecodedObservation:
    image_bytes: bytes | None
    image_format: str | None
    symbolic: dict[str, Any] | None
    metadata: dict[str, Any]


def decode_observation(payload: bytes | str | dict[str, Any]) -> DecodedObservation:
    source_hash = _source_hash(payload)
    if INPUT_KIND == "structured_symbolic":
        symbolic = _decode_symbolic(payload)
        return DecodedObservation(
            image_bytes=None,
            image_format=None,
            symbolic=symbolic,
            metadata=_metadata(source_hash, primary_output="typed_symbolic_observation"),
        )

    if INPUT_KIND == "encoded_image":
        image_bytes = _coerce_bytes(payload)
        image_format = _detect_image_format(image_bytes)
        if image_format is None:
            raise DecodeError("Expected PNG, JPEG, GIF, or WebP image bytes.")
        return DecodedObservation(
            image_bytes=image_bytes,
            image_format=image_format,
            symbolic=None,
            metadata=_metadata(source_hash, primary_output="decoded_image_fixture"),
        )

    if CAN_DECODE_PACKED_4BIT:
        image_bytes = _decode_packed_4bit_to_png(_coerce_bytes(payload))
        return DecodedObservation(
            image_bytes=image_bytes,
            image_format="png",
            symbolic=None,
            metadata=_metadata(source_hash, primary_output="png_image_fixture"),
        )

    raise DecodeError(
        "This generated decoder does not have enough source-grounded evidence to decode "
        f"input_kind={{INPUT_KIND!r}} strategy={{DECODER_STRATEGY!r}}. Review decoder_spec.json "
        "and update the guide/source evidence before VLM labeling raw observations."
    )


def decode_to_vlm_image(payload: bytes | str | dict[str, Any]) -> tuple[bytes, str, dict[str, Any]]:
    decoded = decode_observation(payload)
    if decoded.image_bytes is None or decoded.image_format is None:
        raise DecodeError("Decoded observation does not produce a VLM image fixture.")
    return decoded.image_bytes, decoded.image_format, decoded.metadata


def write_decoded_fixture(payload: bytes | str | dict[str, Any], output_dir: Path) -> dict[str, Any]:
    image_bytes, image_format, metadata = decode_to_vlm_image(payload)
    output_dir.mkdir(parents=True, exist_ok=True)
    fixture_hash = hashlib.sha256(image_bytes).hexdigest()
    image_path = output_dir / f"{{fixture_hash}}.{{image_format}}"
    metadata_path = output_dir / f"{{fixture_hash}}.json"
    image_path.write_bytes(image_bytes)
    metadata_path.write_text(json.dumps(metadata, indent=2, sort_keys=True) + "\\n", encoding="utf-8")
    return {{
        "image_path": str(image_path),
        "metadata_path": str(metadata_path),
        "image_hash": fixture_hash,
        "image_format": image_format,
        "metadata": metadata,
    }}


def _decode_symbolic(payload: bytes | str | dict[str, Any]) -> dict[str, Any]:
    if isinstance(payload, dict):
        return dict(payload)
    if isinstance(payload, bytes):
        payload = payload.decode("utf-8")
    if not isinstance(payload, str):
        raise DecodeError(f"Unsupported symbolic payload type: {{type(payload).__name__}}")
    parsed = json.loads(payload)
    if not isinstance(parsed, dict):
        raise DecodeError("Symbolic observation JSON must decode to an object.")
    return parsed


def _decode_packed_4bit_to_png(payload: bytes) -> bytes:
    if EXPECTED_SOURCE_BYTES is not None and len(payload) != EXPECTED_SOURCE_BYTES:
        raise DecodeError(f"Expected {{EXPECTED_SOURCE_BYTES}} source bytes, got {{len(payload)}}.")
    if FRAME_WIDTH is None or FRAME_HEIGHT is None:
        raise DecodeError("Packed visual decoder is missing frame dimensions.")
    expected_pixels = FRAME_WIDTH * FRAME_HEIGHT
    if len(payload) * 2 != expected_pixels:
        raise DecodeError(
            f"Packed 4-bit payload has {{len(payload) * 2}} pixels, expected {{expected_pixels}}."
        )
    pixels = bytearray()
    for value in payload:
        pixels.append(((value >> 4) & 0x0F) * 17)
        pixels.append((value & 0x0F) * 17)
    return _png_from_grayscale(bytes(pixels), FRAME_WIDTH, FRAME_HEIGHT)


def _png_from_grayscale(pixels: bytes, width: int, height: int) -> bytes:
    if len(pixels) != width * height:
        raise DecodeError("Pixel count does not match PNG dimensions.")
    rows = bytearray()
    for y in range(height):
        rows.append(0)
        start = y * width
        rows.extend(pixels[start : start + width])
    return (
        b"\\x89PNG\\r\\n\\x1a\\n"
        + _png_chunk(b"IHDR", struct.pack(">IIBBBBB", width, height, 8, 0, 0, 0, 0))
        + _png_chunk(b"IDAT", zlib.compress(bytes(rows)))
        + _png_chunk(b"IEND", b"")
    )


def _png_chunk(kind: bytes, data: bytes) -> bytes:
    import binascii

    checksum = binascii.crc32(kind)
    checksum = binascii.crc32(data, checksum) & 0xFFFFFFFF
    return struct.pack(">I", len(data)) + kind + data + struct.pack(">I", checksum)


def _coerce_bytes(payload: bytes | str | dict[str, Any]) -> bytes:
    if isinstance(payload, bytes):
        return payload
    if isinstance(payload, str):
        return payload.encode("utf-8")
    raise DecodeError(f"Expected bytes payload, got {{type(payload).__name__}}.")


def _detect_image_format(image_bytes: bytes) -> str | None:
    if image_bytes.startswith(b"\\x89PNG\\r\\n\\x1a\\n"):
        return "png"
    if image_bytes.startswith(b"\\xff\\xd8\\xff"):
        return "jpeg"
    if image_bytes.startswith((b"GIF87a", b"GIF89a")):
        return "gif"
    if len(image_bytes) >= 12 and image_bytes[:4] == b"RIFF" and image_bytes[8:12] == b"WEBP":
        return "webp"
    return None


def _metadata(source_hash: str, *, primary_output: str) -> dict[str, Any]:
    return {{
        "decoder_name": DECODER_NAME,
        "decoder_version": DECODER_VERSION,
        "decoder_strategy": DECODER_STRATEGY,
        "input_kind": INPUT_KIND,
        "primary_output": primary_output,
        "source_observation_hash": source_hash,
        "width": FRAME_WIDTH,
        "height": FRAME_HEIGHT,
        "source_evidence": DECODER_SPEC.get("evidence", []),
    }}


def _source_hash(payload: bytes | str | dict[str, Any]) -> str:
    if isinstance(payload, bytes):
        data = payload
    elif isinstance(payload, str):
        data = payload.encode("utf-8")
    else:
        data = json.dumps(payload, sort_keys=True, separators=(",", ":")).encode("utf-8")
    return hashlib.sha256(data).hexdigest()
'''


