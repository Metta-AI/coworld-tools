DECODER_SPEC_SCHEMA_VERSION = "maker.decoder_spec.v1"


def generate_decoder_artifacts(
    *,
    bundle: GuideBundle,
    output_dir: Path,
    surface: ObservationSurface,
) -> tuple[Path, ...]:
    spec = build_decoder_spec(bundle=bundle, surface=surface)
    spec_file = output_dir / "agent" / "perception" / "decoder_spec.json"
    task_file = output_dir / "agent" / "perception" / "DECODER_GENERATION_TASK.md"
    decoder_file = output_dir / "agent" / "perception" / "decoder.py"
    init_file = output_dir / "agent" / "perception" / "__init__.py"
    test_file = output_dir / "agent" / "tests" / f"test_{_test_module_prefix(bundle.game_slug, output_dir)}_decoder.py"
    _remove_generated_decoder_tests(output_dir / "agent" / "tests")
    write_json(spec_file, spec)
    write_text(task_file, render_decoder_generation_task(spec))
    write_text(init_file, _render_decoder_init())
    write_text(decoder_file, _render_decoder_impl(spec))
    write_text(test_file, _render_decoder_tests(spec))
    return (spec_file, task_file, init_file, decoder_file, test_file)


def build_decoder_spec(
    *,
    bundle: GuideBundle,
    surface: ObservationSurface,
) -> dict[str, Any]:
    evidence = _extract_decoder_evidence(bundle)
    contract_observation = _contract_observation(bundle)
    contract_evidence = _contract_decoder_evidence(contract_observation)
    evidence = _dedupe_evidence((*contract_evidence, *evidence))
    input_kind = _contract_input_kind(contract_observation) or _classify_decoder_input(surface, evidence)
    hints = _merge_contract_hints(_extract_decoder_hints(evidence), contract_observation)
    strategy = _decoder_strategy(surface, input_kind)
    needs_generated_decoder = strategy in {"generate_symbolic_decoder", "generate_visual_decoder"}
    vlm_ready_without_decoder = input_kind in {"encoded_image", "structured_symbolic"}

    return {
        "schema_version": DECODER_SPEC_SCHEMA_VERSION,
        "game_slug": bundle.game_slug,
        "guide_bundle_hash": bundle.bundle_hash,
        "observation_surface": surface.as_dict(),
        "input_kind": input_kind,
        "decoder_strategy": strategy,
        "needs_generated_decoder": needs_generated_decoder,
        "vlm_ready_without_decoder": vlm_ready_without_decoder,
        "source_documents": [
            "INTERFACE_CONTRACT.md",
            "OBSERVATION_DECODING.md",
            "STATE_AND_VIEW_MODEL.md",
            "MINIMUM_VIABLE_AGENT.md",
        ],
        "evidence": [item.as_dict() for item in evidence],
        "extracted_hints": hints,
        "output_contract": _output_contract(surface, input_kind),
        "implementation_requirements": _implementation_requirements(surface, input_kind),
        "validation_checks": _validation_checks(surface, input_kind),
        "generalization_rule": (
            "Decoder code must be generated from this game bundle's guide docs and source evidence. "
            "Do not reuse assumptions from another game's framebuffer, palette, dimensions, transport, "
            "or observation schema unless those assumptions are rediscovered for this game."
        ),
    }


def render_decoder_generation_task(spec: dict[str, Any]) -> str:
    evidence_lines = "\n".join(
        f"- `{item['document']}:{item['line']}`: {item['text']}" for item in spec["evidence"]
    ) or "- No decoder-specific evidence was found in the guide bundle."
    hints = spec["extracted_hints"]
    hint_lines = "\n".join(
        f"- {key}: {', '.join(str(value) for value in values) if values else 'none'}"
        for key, values in hints.items()
    )
    requirements = "\n".join(f"- {item}" for item in spec["implementation_requirements"])
    checks = "\n".join(f"- {item}" for item in spec["validation_checks"])

    return f"""# Decoder Generation Task

Generated by `maker_v1`.

This is a generated artifact. It describes what a game-specific observation
decoder must do before VLM labeling or parser generation can be reliable.

## Decoder Boundary

- Game: `{spec["game_slug"]}`
- Observation surface: `{spec["observation_surface"]["category"]}`
- Input kind: `{spec["input_kind"]}`
- Strategy: `{spec["decoder_strategy"]}`
- Needs generated decoder: `{spec["needs_generated_decoder"]}`
- VLM-ready without decoder: `{spec["vlm_ready_without_decoder"]}`

## Generalization Rule

{spec["generalization_rule"]}

## Source Evidence

{evidence_lines}

## Extracted Hints

{hint_lines}

## Output Contract

```json
{_jsonish(spec["output_contract"])}
```

## Implementation Requirements

{requirements}

## Validation Checks

{checks}

## Next Step

Use this task plus the guide bundle and game source to generate decoder code
under `agent/perception/`. If the guide evidence is incomplete, inspect the
source and update the generated guide or this decoder spec before writing code.
"""


def _extract_decoder_evidence(bundle: GuideBundle, *, limit: int = 20) -> tuple[Evidence, ...]:
    target_docs = (
        "INTERFACE_CONTRACT.md",
        "OBSERVATION_DECODING.md",
        "STATE_AND_VIEW_MODEL.md",
        "MINIMUM_VIABLE_AGENT.md",
    )
    evidence: list[Evidence] = []
    for filename in target_docs:
        document = bundle.get(filename)
        if document is None:
            continue
        for line_no, line in enumerate(document.content.splitlines(), start=1):
            cleaned = _clean_line(line)
            if not cleaned or _is_low_signal_line(cleaned):
                continue
            lowered = cleaned.lower()
            if any(pattern.search(lowered) for pattern in _DECODER_PATTERNS):
                evidence.append(Evidence(filename, line_no, cleaned))
                if len(evidence) >= limit:
                    return tuple(evidence)
    return tuple(evidence)


def _contract_observation(bundle: GuideBundle) -> dict[str, Any] | None:
    if bundle.contract is None:
        return None
    observation = bundle.contract.get("observation")
    return observation if isinstance(observation, dict) else None


def _contract_decoder_evidence(observation: dict[str, Any] | None) -> tuple[Evidence, ...]:
    if observation is None:
        return ()
    evidence_items: list[dict[str, Any]] = []
    primary = observation.get("primary")
    if isinstance(primary, dict):
        raw = primary.get("evidence")
        if isinstance(raw, list):
            evidence_items.extend(item for item in raw if isinstance(item, dict))
    raw = observation.get("evidence")
    if isinstance(raw, list):
        evidence_items.extend(item for item in raw if isinstance(item, dict))

    evidence: list[Evidence] = []
    for item in evidence_items:
        document = item.get("document")
        line = item.get("line")
        text = item.get("text")
        if not isinstance(document, str) or not isinstance(text, str):
            continue
        try:
            line_no = int(line)
        except (TypeError, ValueError):
            continue
        evidence.append(Evidence(document, line_no, text))
    return tuple(evidence[:20])


def _contract_input_kind(observation: dict[str, Any] | None) -> str | None:
    if observation is None:
        return None
    primary = observation.get("primary")
    if not isinstance(primary, dict):
        return None
    input_kind = primary.get("input_kind")
    if input_kind in {
        "structured_symbolic",
        "encoded_image",
        "raw_visual_observation",
        "mixed_visual_or_symbolic_unknown",
        "visual_unknown_encoding",
    }:
        return str(input_kind)
    return None


def _merge_contract_hints(
    hints: dict[str, list[str]],
    observation: dict[str, Any] | None,
) -> dict[str, list[str]]:
    if observation is None:
        return hints
    primary = observation.get("primary")
    if not isinstance(primary, dict):
        return hints
    merged = {key: list(value) for key, value in hints.items()}
    width = primary.get("width")
    height = primary.get("height")
    if isinstance(width, int) and isinstance(height, int):
        _append_unique(merged["dimensions"], f"{width}x{height}")
    byte_length = primary.get("byte_length")
    if isinstance(byte_length, int):
        _append_unique(merged["byte_lengths"], f"{byte_length} bytes")
    bit_depth = primary.get("bit_depth")
    if isinstance(bit_depth, int):
        _append_unique(merged["bit_depths"], f"{bit_depth}-bit")
    transport = primary.get("transport")
    if isinstance(transport, str):
        for term in transport.split("_"):
            _append_unique(merged["transport_terms"], term)
    encoding = primary.get("encoding")
    if isinstance(encoding, str):
        for term in encoding.split("_"):
            _append_unique(merged["codec_terms"], term)
    return merged


def _append_unique(values: list[str], value: str) -> None:
    if value and value not in values:
        values.append(value)


def _dedupe_evidence(items: tuple[Evidence, ...]) -> tuple[Evidence, ...]:
    seen: set[tuple[str, int, str]] = set()
    result: list[Evidence] = []
    for item in items:
        key = (item.document, item.line, item.text)
        if key in seen:
            continue
        seen.add(key)
        result.append(item)
    return tuple(result)


def _classify_decoder_input(surface: ObservationSurface, evidence: tuple[Evidence, ...]) -> str:
    text = "\n".join(item.text.lower() for item in evidence)
    if surface.category == "symbolic_primary":
        return "structured_symbolic"
    if _has_encoded_image_evidence(text) and not _has_raw_visual_evidence(text):
        return "encoded_image"
    if _has_raw_visual_evidence(text):
        return "raw_visual_observation"
    if surface.category == "mixed_or_alternate":
        return "mixed_visual_or_symbolic_unknown"
    if surface.category == "visual_primary":
        return "visual_unknown_encoding"
    return "unknown"


def _decoder_strategy(surface: ObservationSurface, input_kind: str) -> str:
    if input_kind == "structured_symbolic":
        return "generate_symbolic_decoder"
    if input_kind == "encoded_image":
        return "pass_through_encoded_image"
    if input_kind in {"raw_visual_observation", "visual_unknown_encoding", "mixed_visual_or_symbolic_unknown"}:
        return "generate_visual_decoder"
    if surface.category in {"visual_primary", "mixed_or_alternate"}:
        return "generate_visual_decoder"
    return "defer_until_observation_contract_is_known"


def _extract_decoder_hints(evidence: tuple[Evidence, ...]) -> dict[str, list[str]]:
    text = "\n".join(item.text for item in evidence)
    return {
        "dimensions": _unique(re.findall(r"\b\d{2,5}\s*[xX×]\s*\d{2,5}\b", text)),
        "byte_lengths": _unique(re.findall(r"\b\d[\d,]*\s+bytes?\b", text, flags=re.I)),
        "bit_depths": _unique(re.findall(r"\b\d+\s*[- ]?bit\b", text, flags=re.I)),
        "image_formats": _unique(re.findall(r"\b(?:png|jpeg|jpg|webp|gif)\b", text, flags=re.I)),
        "transport_terms": _unique(
            term
            for term in ("BinaryMessage", "binary", "JSON", "websocket", "token", "array")
            if re.search(rf"\b{re.escape(term)}\b", text, flags=re.I)
        ),
        "codec_terms": _unique(
            term
            for term in ("packed", "framebuffer", "frame buffer", "palette", "sprite", "render", "pixels")
            if re.search(rf"\b{re.escape(term)}\b", text, flags=re.I)
        ),
    }


def _output_contract(surface: ObservationSurface, input_kind: str) -> dict[str, object]:
    if input_kind == "structured_symbolic":
        return {
            "primary_output": "typed_symbolic_observation",
            "vlm_image_fixture": "not_required_for_baseline",
        }
    if input_kind == "encoded_image":
        return {
            "primary_output": "decoded_image_fixture",
            "vlm_image_fixture": "pass_through_original_image_after_format_validation",
            "accepted_image_formats": ["png", "jpeg", "gif", "webp"],
        }
    if surface.category in {"visual_primary", "mixed_or_alternate"}:
        return {
            "primary_output": "png_image_fixture",
            "vlm_image_fixture": "png_image_fixture",
            "metadata": [
                "source_observation_hash",
                "decoder_name",
                "decoder_version",
                "width",
                "height",
                "source_evidence",
            ],
        }
    return {
        "primary_output": "unknown_until_observation_contract_is_resolved",
    }


def _implementation_requirements(surface: ObservationSurface, input_kind: str) -> list[str]:
    if input_kind == "structured_symbolic":
        return [
            "Generate a typed parser for the structured observation fields.",
            "Reject malformed observations with explicit errors or safe fallback state.",
            "No visual VLM image fixture is required for the baseline agent path.",
        ]
    if input_kind == "encoded_image":
        return [
            "Validate that incoming bytes are a supported encoded image format.",
            "Persist the original image or a normalized PNG fixture with stable hashing.",
            "Do not apply framebuffer-specific unpacking unless the source proves it is needed.",
        ]
    if surface.category in {"visual_primary", "mixed_or_alternate"}:
        return [
            "Generate decoder code from the target game's guide docs and source, not from another game's protocol.",
            "Convert raw player observations into PNG fixtures before Bedrock or other image VLM labeling.",
            "Emit metadata that records source hash, output dimensions, decoder version, and source evidence.",
            "Add fixture tests for representative observations and edge cases from the game source.",
        ]
    return [
        "Inspect guide docs and source to resolve the observation contract before generating decoder code.",
    ]


def _validation_checks(surface: ObservationSurface, input_kind: str) -> list[str]:
    if input_kind == "structured_symbolic":
        return [
            "Unit tests cover required fields, optional fields, terminal observations, and malformed input.",
            "Policy smoke tests consume the typed observation without VLM calls.",
        ]
    if input_kind == "encoded_image":
        return [
            "Fixture tests verify supported image formats are accepted and unsupported bytes are rejected.",
            "VLM bootstrap receives image bytes without codec-specific mutation.",
        ]
    if surface.category in {"visual_primary", "mixed_or_alternate"}:
        return [
            "Fixture tests prove raw observations decode to deterministic image hashes.",
            "Decoded image dimensions match source-derived expectations when known.",
            "Bedrock or another image VLM accepts the decoded fixture format.",
            "Decoder failures are surfaced before spending VLM budget.",
        ]
    return [
        "No decoder code should be generated until the observation contract is known.",
    ]


def _has_raw_visual_evidence(text: str) -> bool:
    return any(pattern.search(text) for pattern in _RAW_VISUAL_PATTERNS)


def _has_encoded_image_evidence(text: str) -> bool:
    return any(pattern.search(text) for pattern in _ENCODED_IMAGE_PATTERNS)


def _unique(values: Any) -> list[str]:
    result: list[str] = []
    for value in values:
        normalized = str(value).strip()
        if not normalized or normalized in result:
            continue
        result.append(normalized)
    return result


